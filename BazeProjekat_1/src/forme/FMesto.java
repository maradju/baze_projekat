    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
            String MestoID = jtfMestoID.getText().trim();
            if (MestoID.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Niste popunili sva polja", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if (!MestoID.matches("[1-9][0-9]*")) {
                JOptionPane.showMessageDialog(null, "ID mora da bude broj", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Mesto m = new Mesto();
            Drzava d = new Drzava();
            d.setDrzavaID(Integer.parseInt(jtfDrzavaID.getText()));
            d.setNazivDrzave(jtfNazivDrzave.getText());
            m.setMestoID(Integer.parseInt(MestoID));
            m.setDrzavaID(d);
            m.setNazivMesta(jtfNazivMesta.getText());
            
            
            
            try {
                dbb.uspostaviKonekciju();
                dbb.ubaciMesto(m);
                dbb.potvrdiTransakciju();
                dbb.raskiniKonekciju();
                JOptionPane.showMessageDialog(null, "Uspešno ubacen mesto", "mesto", JOptionPane.INFORMATION_MESSAGE);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FReklamacija.class.getName()).log(Level.SEVERE, null, ex);
                dbb.ponistiTransakciju();
                dbb.raskiniKonekciju();
                JOptionPane.showMessageDialog(null, "Neuspešno ubacivanje mesta\n"+ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
            }
            dispose();
    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void btnPronadjiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPronadjiActionPerformed
        try {     
        
            dbb.uspostaviKonekciju();
      
            Mesto m = dbb.uitajMesta(Integer.parseInt(jtfMestoID.getText()));
            if (m == null) {
                JOptionPane.showMessageDialog(null, "Nije pronadjeno mesto sa datim rb", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            dbb.potvrdiTransakciju();
            dbb.raskiniKonekciju();
            jtfDrzavaID.setText(m.getDrzavaID().getDrzavaID() + "");
            jtfNazivDrzave.setText(m.getDrzavaID().getNazivDrzave());
            jtfNazivMesta.setText(m.getNazivMesta());


            
 
            btnPronadji.setEnabled(false);
            jtfNazivMesta.setEnabled(true);
            btnIzmeni.setEnabled(true);
            btnObrisi.setEnabled(true);
            } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FMesto.class.getName()).log(Level.SEVERE, null, ex);
            dbb.ponistiTransakciju();
            dbb.raskiniKonekciju();
            JOptionPane.showMessageDialog(null, "Neuspešno pretrazivanje mesta\n"+ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPronadjiActionPerformed

