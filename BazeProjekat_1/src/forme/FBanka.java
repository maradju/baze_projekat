    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
            String bankaID = jtfBankaID.getText().trim();
            if (bankaID.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Niste popunili sva polja", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if (!bankaID.matches("[1-9][0-9]*")) {
                JOptionPane.showMessageDialog(null, "ID mora da bude broj", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Banka b = new Banka();
            
            b.setBankaID(Integer.parseInt(bankaID));
            b.setNazivBanke(jtfNazivBanke.getText());
            b.setDrzavaID((Drzava) cmbDrzava.getSelectedItem());
            b.setMestoID((Mesto) cmbMesto.getSelectedItem());
            jtfDrzavaID.setText(b.getDrzavaID().getDrzavaID()+ "");
            jtfMesrtoID.setText(b.getMestoID().getMestoID() + "");
            jtfNazivDrzave.setText(b.getDrzavaID().getNazivDrzave());
            jtfNazivMesta.setText(b.getMestoID().getNazivMesta());
            
            try {
                dbb.uspostaviKonekciju();
                dbb.ubaciBanku(b);
                dbb.potvrdiTransakciju();
                dbb.raskiniKonekciju();
                JOptionPane.showMessageDialog(null, "Uspešno ubacen banka", "Banka", JOptionPane.INFORMATION_MESSAGE);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FReklamacija.class.getName()).log(Level.SEVERE, null, ex);
                dbb.ponistiTransakciju();
                dbb.raskiniKonekciju();
                JOptionPane.showMessageDialog(null, "Neuspešno ubacivanje banke\n"+ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
            }
            dispose();
    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void btnPronadjiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPronadjiActionPerformed
        try {     
            if (jtfBankaID.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Niste popunili sva polja za pretragu", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if (!jtfBankaID.getText().matches("[1-9][0-9]*")) {
                JOptionPane.showMessageDialog(null, "ID mora da bude broj", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
              
            dbb.uspostaviKonekciju();
      
            Banka b = dbb.ucitajBanku(Integer.parseInt(jtfBankaID.getText()));
            if (b == null) {
                JOptionPane.showMessageDialog(null, "Nije pronadjen izvod sa datim rb", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            dbb.potvrdiTransakciju();
            dbb.raskiniKonekciju();
            jtfBankaID.setText(b.getBankaID()+"");
            jtfNazivBanke.setText(b.getNazivBanke());
            popuniComboBoxDrzava();
            popuniComboBoxMesto();
            for (int i = 0; i < cmbMesto.getItemCount(); i++) {
                if (cmbMesto.getItemAt(i).toString().equals(b.getMestoID().getNazivMesta())) {
                    System.out.println("pronasao mesto");
                    cmbMesto.setSelectedItem(cmbMesto.getItemAt(i));
                }
            }
            popuniComboBoxDrzava();
            for (int i = 0; i < cmbDrzava.getItemCount(); i++) {
                if (cmbDrzava.getItemAt(i).toString().equals(b.getDrzavaID().getNazivDrzave())) {
                    System.out.println("pronasao drzavu");
                    cmbDrzava.setSelectedItem(cmbDrzava.getItemAt(i));
                }
            }

            jtfDrzavaID.setText(b.getDrzavaID().getDrzavaID()+ "");
            jtfMesrtoID.setText(b.getMestoID().getMestoID() + "");
            jtfNazivDrzave.setText(b.getDrzavaID().getNazivDrzave());
            jtfNazivMesta.setText(b.getMestoID().getNazivMesta());
            jtfBankaID.setEnabled(false);
            btnPronadji.setEnabled(false);
            jtfNazivBanke.setEnabled(true);
            cmbMesto.setEnabled(true);
            cmbDrzava.setEnabled(true);
            btnIzmeni.setEnabled(true);
            btnObrisi.setEnabled(true);
            } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FBanka.class.getName()).log(Level.SEVERE, null, ex);
            dbb.ponistiTransakciju();
            dbb.raskiniKonekciju();
            JOptionPane.showMessageDialog(null, "Neuspešno pretrazivanje izvoda\n"+ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPronadjiActionPerformed

