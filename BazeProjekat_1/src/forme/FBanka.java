    private void popuniComboBoxDrzava() {
         try {
            cmbDrzava.removeAllItems();
            dbb.uspostaviKonekciju();
            List<Drzava> lista = dbb.vratiSveDrzave();
            dbb.potvrdiTransakciju();
            dbb.raskiniKonekciju();
            for (Drzava drzava : lista) {
                cmbDrzava.addItem(drzava);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FBanka.class.getName()).log(Level.SEVERE, null, ex);
            dbb.ponistiTransakciju();
            dbb.raskiniKonekciju();
        }
    }

    private void popuniComboBoxMesto() {
        try {
            cmbMesto.removeAllItems();
            dbb.uspostaviKonekciju();
            List<Mesto> lista = dbb.vratiSvaMesta();
            dbb.potvrdiTransakciju();
            dbb.raskiniKonekciju();
            for (Mesto m : lista) {
                cmbMesto.addItem(m);
            }
            ComboBoxModel model = new DefaultComboBoxModel(lista.toArray());
            cmbMesto.setModel(model);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FBanka.class.getName()).log(Level.SEVERE, null, ex);
            dbb.ponistiTransakciju();
            dbb.raskiniKonekciju();
        }
    }
